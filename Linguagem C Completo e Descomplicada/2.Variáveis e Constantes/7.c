//7) Elabore um programa que leia um caractere e depois o imprima como um valor

#include <stdio.h>

int main(){
    char c;
    printf("Digite um caractere: ");
    scanf("%c",&c);
    printf("Valor do caractere: %d\n",c);
    return 0;
}

//Os caracteres são representados como números na tabela ASCII ou em outras codificações de caracteres. Cada caractere na tabela ASCII é associado a um número inteiro
